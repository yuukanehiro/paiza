def get_square_division_bucket(items: List[int]) -> List[int]:
    """
    平方分割のバケット（各ブロックの最大値）とサイズを返す
    """
    n = len(items)
    size = int(pow(n, 0.5))
    if size * size < n:
        size += 1  # 切り上げ

    bucket = [-1] * size
    for i in range(size):
        start, end = size * i, size * (i + 1)
        bucket[i] = max(items[start:end])

    return bucket


