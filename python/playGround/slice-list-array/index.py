

A = ["A", "B", "C", "D", "E"]
print(len(A))  # Output: 5

print(A[0])  # Output: A
print(A[1])  # Output: B
print(A[2])  # Output: C
print(A[3])  # Output: D
print(A[4])  # Output: E

print(A[-1])  # Output: E
print(A[-2])  # Output: D
print(A[-3])  # Output: C
print(A[-4])  # Output: B
print(A[-5])  # Output: A

print(A[0:2])  # Output: ["A", "B"]

# 最後の要素
print(A[len(A) - 1]) # Output: E


